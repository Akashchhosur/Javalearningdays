class ArithmeticOperators
{
 	public static void main(String[] args)
	{
		int a=10;
		int b=20;
		int sum=a+b;
		
		System.out.println("SUM IS :"+sum);//output = 30
		System.out.println(b-a);//output = 10
		System.out.println(a+b);//output = 30
		System.out.println(a*b);//output = 200	
		System.out.println(10/5);//output = 2
		System.out.println(10%5);//output = 0
		
		System.out.println("---------------------------------");
		
		int x=10;
		System.out.println("X: "+x);//output = x:10
		x +=3;  //x=x+3
		System.out.println("x: "+x);//output = x:13

	}
}