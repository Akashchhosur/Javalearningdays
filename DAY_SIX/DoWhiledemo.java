class DoWhiledemo
{
	public static void main(String[] args)
	{
		//number 1 to 5
		int n=1;
		do
		{
			System.out.println(n);
			n++;
		}
		while(n<=5);
		
		System.out.println("----------------------");
		//reverse Number 5 to 1
		int i=5;
		do
		{
			System.out.println(i);
			i--;
		}
		while(i>=1);
		


		
	}
}