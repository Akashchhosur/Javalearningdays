package com;

class Cd_11 {
	public static void main(String[] args) {
		//n Number tables Tables in Ordered way
		int n=5;
		for(int i=1;i<=10;i++) {
			System.out.printf("%d X %d = %d\n",n,i,n*i);
			
		}
		System.out.println("---------------------------");
		//Tables reverse Order
		int m=5;
		for(int i=10;i>0;i--) {
			System.out.printf("%d X %d = %d\n",n,i,n*i);
			
		}
	}

}
