package com;

class Reversetraversing {
	public static void main(String[] args) {
		int a[]= {10,20,30};
		for(int i=a.length-1;i>=0;i--) {
			System.out.println(a[i]);
		}
		System.out.println("=====================");
		double marks[]= {100,60.5,35.1};
		for(int i=marks.length-1;i>=0;i--) {
			System.out.println(marks[i]);
		}
	}

}
